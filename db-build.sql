set foreign_key_checks = 0;
drop table if exists catalog_sources;
drop table if exists equivalencies;
drop table if exists minor_requirements;
drop table if exists major_requirements;
drop table if exists minors;
drop table if exists majors;
drop table if exists courses;
drop table if exists departments;
set foreign_key_checks = 1;

create table departments (
    department_id int primary key,
    department_name varchar(150) not null unique
);

create table courses (
    course_id int primary key,
    department_id int not null,
    course_code varchar(20) not null unique,
    course_title varchar(220) not null,
    semester_hours int not null default 4,
    catalog_url varchar(700),
    foreign key (department_id) references departments(department_id)
);

create table majors (
    major_id int primary key,
    department_id int not null,
    major_name varchar(180) not null unique,
    catalog_url varchar(700),
    foreign key (department_id) references departments(department_id)
);

create table minors (
    minor_id int primary key,
    department_id int not null,
    minor_name varchar(180) not null unique,
    catalog_url varchar(700),
    foreign key (department_id) references departments(department_id)
);

create table major_requirements (
    requirement_id int primary key,
    major_id int not null,
    course_id int not null,
    requirement_type varchar(20) not null,
    requirement_group varchar(240),
    notes varchar(800),
    foreign key (major_id) references majors(major_id),
    foreign key (course_id) references courses(course_id)
);

create table minor_requirements (
    requirement_id int primary key,
    minor_id int not null,
    course_id int not null,
    requirement_type varchar(20) not null,
    requirement_group varchar(240),
    notes varchar(800),
    foreign key (minor_id) references minors(minor_id),
    foreign key (course_id) references courses(course_id)
);

create table equivalencies (
    equivalency_id int primary key,
    course_a_id int not null,
    course_b_id int not null,
    equivalency_type varchar(90) not null,
    explanation varchar(900) not null,
    source_context varchar(260),
    source_url varchar(700),
    foreign key (course_a_id) references courses(course_id),
    foreign key (course_b_id) references courses(course_id)
);

create table catalog_sources (
    source_id int primary key,
    source_name varchar(200) not null,
    source_url varchar(700) not null,
    notes varchar(800)
);

